    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Tombol hapus
        if (jTable1.getSelectedRow() == -1) {
            // Jika tidak ada baris dari jTable1 yang terpilih (terseleksi)
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih salah satu baris yang akan dihapus!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            // Jika ada baris dari jTable1 yang terpilih
            int pilihan = JOptionPane.showConfirmDialog(
                    this,
                    "Apakah yakin akan menghapus data?",
                    "Konfirmasi",
                    JOptionPane.YES_NO_OPTION
            );
            if (pilihan == 0) { // tombol yes dipilih
                // menghapus baris dari jTable1 yang terpilih
                model.removeRow(jTable1.getSelectedRow());
                jButton5ActionPerformed(evt);
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

