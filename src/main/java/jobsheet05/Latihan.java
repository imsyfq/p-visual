    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Tombol Simpan
        // validasi NIM tidak boleh kosong
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "NIM tidak boleh kosong!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );
            jTextField1.requestFocus();
            return;
        }

        // validasi nama tidak boleh kosong
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this,
                    "Nama tidak boleh kosong!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );
            return;
        }

        // validasi NIM harus unik
        int rows = model.getRowCount();
        // System.out.println("Jumlah baris pada table : " + rows);
        int i = 0;
        while (i < rows) {
            String nim = model.getValueAt(i, 0).toString();
            // System.out.println("NIM pada table : " + nim);

            if (jTextField1.getText().equals(nim)) {
                JOptionPane.showMessageDialog(
                        this,
                        "NIM yang Anda masukkan telah ada sebelumnya!",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE
                );
                return;
            }
            i++;
        }
        if (modeSimpan == true) {
            // simpan data baru
            model.addRow(
                    // menambahkan objek Object dalam bentuk array satu dimensi
                    // yang berisi data nim dan nama
                    new Object[]{
                        jTextField1.getText(), // data nim
                        jTextField2.getText() // data nama
                    }
            );
        } else {
            jTable1.setValueAt(
                    // memberikan (mengubah) nilai pada baris dan kolom tertentu
                    jTextField1.getText(),
                    jTable1.getSelectedRow(),
                    0
            );
            // nama
            jTable1.setValueAt(
                    // memberikan (mengubah) nilai pada baris dan kolom tertentu
                    jTextField2.getText(),
                    jTable1.getSelectedRow(),
                    1
            );
        }

        // memanggil (invoke) method jButton5ActionPerformed(evt);
        // yang nanti akan didefinisikan (ditetapkan) setelahnya untuk jButton5
        jButton5ActionPerformed(evt);
    }//GEN-LAST:event_jButton2ActionPerformed

