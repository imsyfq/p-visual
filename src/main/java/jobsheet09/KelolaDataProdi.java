    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // pengecekan apakah tombol enter di keyboard telah ditekan
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            dataDitemukan = cariData(jTextField1.getText());
            if (!dataDitemukan) {
                //jTextField4.setEnabled(true);
                jTextField3.setEnabled(true);
                jTextField2.setEnabled(true);
                jTextField2.requestFocus();

                jButton1.setEnabled(true);
                jButton3.setEnabled(true);
            } else {
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(this,
                        "Kode Prodi " + jTextField1.getText() + " sudah ada! "
                        + "Apakah Anda ingin menghapus / mengubah data?",
                        "Konfirmasi",
                        JOptionPane.YES_NO_OPTION
                );

                if (pilihan == 0) { // yes
                    try {
                        jTextField2.setText(rs.getString("nama_prodi"));
                        jTextField3.setText(rs.getString("singkatan"));
                        this.setJurusan(
                                rs.getString("nama_jurusan"),
                                rs.getString("kode_jurusan")
                        );

                        modeSimpan = false;
                        jButton3.setEnabled(false);
                        jButton4.setEnabled(true);
                        jButton5.setEnabled(true);
                        jButton6.setEnabled(true);

                        jTextField1.setEnabled(false);
                    } catch (SQLException ex) {
                        System.err.println("Query error");
                    }
                } else {
                    jTextField1.setText("");
                    jTextField1.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

