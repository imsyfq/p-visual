    private boolean cariMahasiswa(String data) {
        try {
            rs = perintah.executeQuery("select kode_prodi from mhs"
                    + " where kode_prodi = '" + data + "' limit 1");
            return rs.next();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }

        return false;
    }


    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if ((jTable1.getSelectedRow() == -1) && (!dataDitemukan)) {
            JOptionPane.showMessageDialog(
                    this,
                    "Silahkan pilih baris yang akan diubah",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            int pilihan = JOptionPane.showConfirmDialog(
                    this,
                    "Apakah anda yakin akan menghapus data?",
                    "Konfirmasi",
                    JOptionPane.YES_NO_OPTION
            );

            if (pilihan == 0) {
                if (cariMahasiswa(jTextField1.getText())) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Tidak dapat menghapus data prodi "
                            + "dikarenakan telah digunakan pada mahasiswa",
                            "Informasi",
                            JOptionPane.INFORMATION_MESSAGE);
                    return;
                }

                try {
                    perintah.execute("delete from prodi where kode_prodi = '"
                            + jTextField1.getText() + "'"
                    );

                    tampilData();
                } catch (SQLException ex) {
                    System.err.println(ex.getMessage());
                }

                bersihData();
                reset();
            }

        }
    }//GEN-LAST:event_jButton5ActionPerformed

