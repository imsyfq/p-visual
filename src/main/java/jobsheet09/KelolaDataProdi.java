    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // validasi data
        if (jTextField1.getText().isEmpty()
                || jTextField2.getText().isEmpty()
                || jTextField3.getText().isEmpty()
                || jTextField4.getText().isEmpty()
                || this.kode_jurusan == null) {
            JOptionPane.showMessageDialog(
                    this,
                    "Harap lengkapi data!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
            );

            return;
        }

        if (modeSimpan) {
            if (cariData(jTextField1.getText())) {
                JOptionPane.showMessageDialog(
                        this,
                        "Penyimpanan data jurusan gagal dilakukan"
                        + ", karena kode prodi " + jTextField1.getText()
                        + " telah ada di table",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                try {
                    perintah.execute("insert into prodi values ('"
                            + jTextField1.getText() + "','"
                            + jTextField2.getText() + "','"
                            + jTextField3.getText() + "','"
                            + this.kode_jurusan + "');"
                    );

                    tampilData();
                } catch (SQLException ex) {
                    System.err.println(ex.getMessage());
                }
            }
        } else {
            if ((jTable1.getSelectedRow() == -1) && (!dataDitemukan)) {
                JOptionPane.showMessageDialog(
                        this,
                        "Silahkan pilih baris yang akan diubah",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                try {
                    perintah.execute("update prodi set "
                            + "nama_prodi ='" + jTextField2.getText()
                            + "', singkatan ='" + jTextField3.getText()
                            + "', kode_jurusan ='" + this.kode_jurusan
                            + "' where kode_prodi = '" + jTextField1.getText() + "'");

                    tampilData();
                } catch (SQLException ex) {
                    System.err.println("Query error");
                }
            }
        }

        this.reset();
        this.bersihData();
    }//GEN-LAST:event_jButton3ActionPerformed

