    private boolean cariData(String data) {
        try {
            rs = perintah.executeQuery(
                    "select prodi.*, nama_jurusan from prodi "
                    + "inner join jurusan on "
                    + "jurusan.kode_jurusan = prodi.kode_jurusan "
                    + "where kode_prodi = '" + data + "'");
            return rs.next();
        } catch (SQLException ex) {
            System.err.println("Query Error");
        }

        return false;
    }

