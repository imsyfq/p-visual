    private void tampilData() {
        try {
            rs = perintah.executeQuery(
                    "select prodi.*, jurusan.nama_jurusan from prodi "
                    + "inner join jurusan on "
                    + "jurusan.kode_jurusan = prodi.kode_jurusan"
            );
            model.setRowCount(0);
            model.fireTableDataChanged();
            while (rs.next()) {
                model.addRow(
                        new Object[]{
                            rs.getString("kode_prodi"),
                            rs.getString("nama_prodi"),
                            rs.getString("singkatan"),
                            rs.getString("kode_jurusan"),
                            rs.getString("nama_jurusan")
                        }
                );
            }

        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }

